import sys


# Задание 1: Считать из параметров командной строки одномерный массив

args = sys.argv[1:]

# Проверка на наличие переданных аргументов
if not args:
    print("Ошибка: не переданы элементы массива.")
    print("Пожалуйста, запустите программу с аргументами, например:")
    print("python lab_3_3.py 10 20 5 30 3")
    exit()

# Преобразование строковых аргументов в целые числа
try:
    array = [int(x) for x in args]
except ValueError:
    print("Ошибка: все элементы массива должны быть целыми числами.")
    print("Пример корректного ввода: python lab_3_3.py 10 20 5 30 3")
    exit()

print(f"Исходный массив: {array}")

# Задание 2: Вычислить сумму элементов с нечетными индексами

sum_odd_indices = 0

for index in range(len(array)):
    if index % 2 != 0:
        sum_odd_indices += array[index]

# Задание 3: Вывести в консоль полученную сумму

print(f"Сумма элементов с нечетными индексами: {sum_odd_indices}")


# Задание 4: Заменить элементы меньшие 15 их удвоенными значениями

for index in range(len(array)):
    if array[index] < 15:
        array[index] *= 2

# Задание 5: Вывести в консоль полученный массив

print(f"Преобразованный массив: {array}")